									<h2 id="four" class="major">Sobre nós</h2>
									<p>A HL Softwares foi formada por dois estudantes do <a href="https://www.unasp.br/" target="new_blank">UNASP</a>, Higor Aparecido Lochi de Almeida e Lucas Passoni de Lima, durante o 3º do ensino médio, com o objetivo de aprender mais sobre programação e de se tornarem os melhores desenvolvedores na área da tecnologia da informação. A formação da HL Softwares foi concluída com o primeiro projeto terminado: um site para a <a href="https://www.hiatec.com.br/" target="new_blank">Hiatec</a> que foi completado com a finalidade de melhorias estéticas e de um sistema web próprio para cadastros e manipulação de dados, como informações de clientes e suas respectivas máquinas, além de registrar ordens de serviços.</p>
